	public static void drawFirmaWindow(InfoFileRecord pInfoFileRecord, String uri, boolean remoteUri, final boolean showValidita, String dataRif) {
		if (pInfoFileRecord.isFirmato() && pInfoFileRecord.getInfoFirma() != null && !pInfoFileRecord.getInfoFirma().equals("")) {
			InfoFirmaWindow lInfoFirmaWindow = new InfoFirmaWindow(pInfoFileRecord, showValidita);
			lInfoFirmaWindow.show();
		} else {
			GWTRestService<Object, Object> lGwtRestService = new GWTRestService<Object, Object>("CalcolaFirmaDatasource");
			Record lRecord = new Record();
			Record record = new Record();
			record.setAttribute("uri", uri);
			record.setAttribute("remote", remoteUri);
			record.setAttribute("dataRif", dataRif);
			lRecord.setAttribute("value", record);
			lGwtRestService.call(lRecord, new ServiceCallback<Record>() {

				@Override
				public void execute(Record object) {
					
					InfoFileRecord info = InfoFileRecord.buildInfoFileRecord(object.getJsObj());
					InfoFirmaWindow lInfoFirmaWindow = new InfoFirmaWindow(info, showValidita);
					lInfoFirmaWindow.show();
				}
			});

		}
	}

