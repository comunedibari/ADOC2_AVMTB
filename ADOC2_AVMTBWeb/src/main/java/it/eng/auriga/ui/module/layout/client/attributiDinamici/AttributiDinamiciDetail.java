		if (maxItemTitleWidth != null && !"".equals(maxItemTitleWidth)) {
			return Integer.parseInt(maxItemTitleWidth);
		}
		return AttributiDinamiciDetail._MAX_TITLE_WIDTH_TO_ALIGN;
