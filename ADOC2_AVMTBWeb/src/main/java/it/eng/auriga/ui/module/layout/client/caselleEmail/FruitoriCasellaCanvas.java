	@Override
	public void editRecord(Record record) {
		super.editRecord(record);
		if (record.getAttribute("idFruitoreCasella") != null && !"".equals(record.getAttributeAsString("idFruitoreCasella"))) {
			LinkedHashMap<String, String> valueMap = new LinkedHashMap<String, String>();
			valueMap.put(record.getAttribute("idFruitoreCasella"), record.getAttribute("denominazione"));
			fruitoreItem.setValueMap(valueMap);
		}
		GWTRestDataSource fruitoreDS = (GWTRestDataSource) fruitoreItem.getOptionDataSource();
		if (record.getAttribute("idFruitoreCasella") != null && !"".equals(record.getAttributeAsString("idFruitoreCasella"))) {
			fruitoreDS.addParam("idFruitoreCasella", record.getAttributeAsString("idFruitoreCasella"));
		} else {
			fruitoreDS.addParam("idFruitoreCasella", null);
		}
		fruitoreItem.setOptionDataSource(fruitoreDS);
		fruitoreItem.fetchData();
	}

