				File fileCopertina = ModelliUtil.fillTemplateAndConvertToPdf(pNuovaPropostaAtto2Bean.getIdProcess(), pNuovaPropostaAtto2Bean.getUriModCopertina(), pNuovaPropostaAtto2Bean.getTipoModCopertina(), templateValues, getSession());
