	protected String encodeUriComponent(String component) {
		String result = null;      

		result = URL.encodeQueryString(component)   
				.replaceAll("\\%28", "(")                          
				.replaceAll("\\%29", ")")   		
				.replaceAll("\\+", "%20")                          
				.replaceAll("\\%27", "'")   			   
				.replaceAll("\\%21", "!")
				.replaceAll("\\%7E", "~");          

		return result;   

	}

