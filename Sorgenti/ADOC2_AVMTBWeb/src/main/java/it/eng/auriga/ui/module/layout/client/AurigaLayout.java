	public void caricaDatiUtenteLavoro(final DSCallback callback) {
		new GWTRestDataSource("UoCollegateUtenteDatasource", "idUo", FieldType.TEXT).fetchData(null, new DSCallback() {

			@Override
			public void execute(DSResponse response, Object rawData, DSRequest request) {

				Record[] data = response.getData();
				uoCollegateUtenteValueMap = new LinkedHashMap<String, String>();
				destProtEntrataDefaultMap = new LinkedHashMap<String, Record>();
				if (data.length > 0) {
					for (int i = 0; i < data.length; i++) {
						uoCollegateUtenteValueMap.put(data[i].getAttribute("idUo"), data[i].getAttribute("descrizione"));
						boolean flgPreimpDestProtEntrata = data[i].getAttribute("flgPreimpDestProtEntrata") != null && "1".equals(data[i].getAttribute("flgPreimpDestProtEntrata"));
						if(flgPreimpDestProtEntrata) {
							destProtEntrataDefaultMap.put(data[i].getAttribute("idUo"), data[i]);			
						}						
					}
				}

				createConfigUtenteMenu(callback);
			}
		});
	}

	@Override
	public boolean showLogoutButton() {

		final Dictionary dictionary = Dictionary.getDictionary("params");
		String usernameSSO;
		try {
			usernameSSO = dictionary.keySet().contains("usernameSSO") && dictionary.get("usernameSSO") != null ? dictionary.get("usernameSSO") : null;
		} catch (Exception e) {
			usernameSSO = null;
		}
		return usernameSSO == null || "".equals(usernameSSO);
	}

