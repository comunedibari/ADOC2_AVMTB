	protected void generaDocDaModello(boolean flagMostraDatiSensibili) {
		
		Record record = getRecordToSave();	
		final Record attributiDinamiciRecord = detailAttributiAdd.getRecordToSave();
		record.setAttribute("valori", detailAttributiAdd.getMappaValori(attributiDinamiciRecord));
		record.setAttribute("tipiValori", detailAttributiAdd.getMappaTipiValori(attributiDinamiciRecord));
		record.setAttribute("colonneListe", detailAttributiAdd.getMappaColonneListe(attributiDinamiciRecord));
		record.setAttribute("idModello", idModAssDocTask != null && !"".equals(idModAssDocTask) ? idModAssDocTask : "101");
		record.setAttribute("flgMostraDatiSensibili", flagMostraDatiSensibili);
		
		GWTRestDataSource lModelliDocDatasource = new GWTRestDataSource("ModelliDocDatasource");
		lModelliDocDatasource.executecustom("creaModelloDaAttributiDinamici", record, new DSCallback() {
			@Override
			public void execute(DSResponse response, Object rawData, DSRequest request) {
				if (response.getStatus() == DSResponse.STATUS_SUCCESS) {
					Record recordPreview = response.getData()[0];
					if (recordPreview.getAttribute("nomeFile") != null && recordPreview.getAttribute("nomeFile").endsWith(".pdf")) {
						PreviewControl.switchPreview(recordPreview.getAttribute("uri"), false, new InfoFileRecord(recordPreview.getAttributeAsRecord("infoFile")), "FileToExtractBean", recordPreview.getAttribute("nomeFile"));
					}else {
						Record lRecord = new Record();
						lRecord.setAttribute("displayFilename", recordPreview.getAttribute("nomeFile"));
						lRecord.setAttribute("uri", recordPreview.getAttribute("uri"));
						lRecord.setAttribute("sbustato", "false");
						lRecord.setAttribute("remoteUri", "false");
						DownloadFile.downloadFromRecord(lRecord, "FileToExtractBean");
					}
				}				
			}		
		});
	}
	
