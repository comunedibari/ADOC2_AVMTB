		InfoFileRecord info = new InfoFileRecord(record.getAttributeAsRecord("infoFile"));

		mDynamicForm.setValue("improntaVerPreFirma", info.getImpronta());
		mDynamicForm.setValue("infoFileVerPreFirma", info);
		
