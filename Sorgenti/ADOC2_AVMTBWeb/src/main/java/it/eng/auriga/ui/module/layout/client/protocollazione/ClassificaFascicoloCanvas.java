	public class FascicoloValidator extends CustomValidator {

		public FascicoloValidator() {
			setErrorMessage("Fascicolo inesistente");
		}

		@Override
		protected boolean condition(Object value) {
			Boolean lBoolean = (Boolean) getFormItem().getForm().getField("setted").getValue();
			String nroFascicolo = (String) getFormItem().getForm().getField("nroFascicolo").getValue();
			String annoFascicolo = (String) getFormItem().getForm().getField("annoFascicolo").getValue();
			String nroSottofascicolo = (String) getFormItem().getForm().getField("nroSottofascicolo").getValue();
			String nroInserto = (String) getFormItem().getForm().getField("nroInserto").getValue();
			String nomeFascicolo = (String) getFormItem().getForm().getField("nomeFascicolo").getValue();
			
			if (!isSetted(nroFascicolo) && !isSetted(annoFascicolo) && !isSetted(nroSottofascicolo)  && !isSetted(nroInserto) && !isSetted(nomeFascicolo)) {
				return !((ClassificaFascicoloItem) getItem()).isFascicoloObbligatorio();
			}
			return lBoolean;
		}

		private boolean isSetted(String value) {
			if (value != null && value.trim().length() > 0 && !value.trim().equals(""))
				return true;
			else
				return false;
		}
	}

