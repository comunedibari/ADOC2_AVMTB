					classificheItem.setOptionDataSource(classificheDS);

					mDynamicForm.setValue("idClassifica", "");
					mDynamicForm.setValue("provCIClassif", "");
					mDynamicForm.setValue("descrizioneClassifica", "");

					mDynamicForm.setValue("indice", "");
