	/**
	 * Metodo che aggiunge i file recuperati dalla scheda note e appunti alla lista dei file principali lFiles e modifica il bean della protocollazione relativo.
	 * @param lFiles Lista attuale dei file. 
	 * @param lDocumentoXmlOutBean  Bean contenente i dati relativi alla protocollazione
	 * @param fileDaAppunti  File recuperati dalla scheda note e appunti. Saranno aggiunti come allegati alla protocollazione
	 */
	private void processFileDaAppunti(Map<String, Object> lFiles, DocumentoXmlOutBean lDocumentoXmlOutBean,List<ItemLavorazioneMailBean> fileDaAppunti) {
		List<AllegatiOutBean> listAllegatiBean = new ArrayList<>(); // lista dove contenere i nuovi file recuperati dagli appunti sottoforma di AllegatiOutBean 
		for(ItemLavorazioneMailBean itemLavorazioneBean : fileDaAppunti) {
			String filename = itemLavorazioneBean.getItemLavNomeFile();
			String uriAllegato = itemLavorazioneBean.getItemLavUriFile();
			String size = itemLavorazioneBean.getItemLavDimensioneFile(); 
			String algoritmoImpronta = itemLavorazioneBean.getItemLavAlgoritmoImprontaFile();
			String encodingImpronta  = itemLavorazioneBean.getItemLavAlgoritmoEncodingImprontaFile();
			String mimeType = itemLavorazioneBean.getItemLavMimeTypeFile();
			String impronta = itemLavorazioneBean.getItemLavImprontaFile();;
			
			lFiles.put(filename.concat(";").concat(size), (String)uriAllegato);  
			AllegatiOutBean allegatoBean = createAllegato(algoritmoImpronta, encodingImpronta, new BigDecimal(size), filename, mimeType, impronta);
			listAllegatiBean.add(allegatoBean);
			
		}
		List<AllegatiOutBean> allegatiOutBeanList = lDocumentoXmlOutBean.getAllegati();
		allegatiOutBeanList.addAll(listAllegatiBean);
		lDocumentoXmlOutBean.setAllegati(allegatiOutBeanList);
	}
	
