	@Override
	public DataSourceField buildListaSceltaOrganigramma(String title, FilterFieldBean lFilterFieldBean,
			Map<String, String> lMapProperty) {
		boolean fromScrivania = getExtraParam().get("fromScrivania") != null && !"".equals(getExtraParam().get("fromScrivania")) ? new Boolean(getExtraParam().get(
				"fromScrivania")) : false;
		String idNode = getExtraParam().get("idNode") != null ? getExtraParam().get("idNode") : "";	
		if(fromScrivania && idNode != null && 
		   (idNode.startsWith("D.2CC") || idNode.startsWith("F.2CC") || 
			idNode.startsWith("D.2NA") || idNode.startsWith("F.2NA") || 
			idNode.startsWith("D.2A") || idNode.startsWith("F.2A"))) {			
			ListaScelta lDataSourceField = new ListaScelta(lFilterFieldBean.getName(), title);
			lDataSourceField.setMultiple(true);
			it.eng.utility.ui.module.layout.client.common.items.SelectItem lSelectItem = 
					new it.eng.utility.ui.module.layout.client.common.items.SelectItem(lFilterFieldBean.getName(), lFilterFieldBean.getTitle());
			GWTRestDataSource lGwtRestDataSource = new GWTRestDataSource("SoggIntCollegatiUoSvFilterDatasource", "key", FieldType.TEXT);
			lGwtRestDataSource.addParam("idNode", idNode);
			lSelectItem.setValueField("key");			
			lSelectItem.setDisplayField("value");		
			lSelectItem.setWidth(550);
			lSelectItem.setOptionDataSource(lGwtRestDataSource);
			lDataSourceField.setEditorType(lSelectItem);
			return lDataSourceField;			
		}		
		return super.buildListaSceltaOrganigramma(title, lFilterFieldBean, lMapProperty);		
	}
	
